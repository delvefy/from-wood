// Downloads the game-icons.net SVGs listed in src/content/icon-map.json and
// regenerates src/content/iconPaths.ts (which IS committed, so builds stay
// offline). Run after changing the map: npm run fetch-icons
//
// Raw repo files are white-on-black: a 512x512 background path followed by the
// icon paths with fill="#fff". We drop the background and fills so the paths
// inherit currentColor and can be tinted with CSS.
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const MAP_FILE = join(ROOT, 'src', 'content', 'icon-map.json');
const OUT_FILE = join(ROOT, 'src', 'content', 'iconPaths.ts');
const RAW_BASE = 'https://raw.githubusercontent.com/game-icons/icons/master';
// Point at a local checkout/download of game-icons/icons to skip the network.
const LOCAL_DIR = process.env.GAME_ICONS_DIR;

const BG_PATH = /^M0 0h512v512H0z$/i;

const map = JSON.parse(readFileSync(MAP_FILE, 'utf8'));
const entries = Object.entries(map).sort(([a], [b]) => a.localeCompare(b));

async function fetchIcon(slug) {
  if (LOCAL_DIR) return readFileSync(join(LOCAL_DIR, `${slug}.svg`), 'utf8');
  const res = await fetch(`${RAW_BASE}/${slug}.svg`);
  if (!res.ok) throw new Error(`${slug}: HTTP ${res.status}`);
  return res.text();
}

function extractPaths(id, slug, svg) {
  // Anything beyond plain <path d> (groups, transforms, circles…) would be
  // dropped silently — refuse instead so a human picks a different icon.
  const stripped = svg
    .replace(/<\/?svg[^>]*>/g, '')
    .replace(/<path[^>]*>/g, '')
    .trim();
  if (stripped) throw new Error(`${id} (${slug}): non-path content: ${stripped.slice(0, 80)}`);

  const paths = [];
  for (const tag of svg.matchAll(/<path([^>]*)>/g)) {
    const attrs = tag[1];
    const d = attrs.match(/\bd="([^"]+)"/)?.[1];
    if (!d) throw new Error(`${id} (${slug}): path without d`);
    if (BG_PATH.test(d)) continue; // background plate
    const extra = attrs.replace(/\b(d|fill)="[^"]*"/g, '').trim().replace(/\/$/, '').trim();
    if (extra) throw new Error(`${id} (${slug}): unsupported path attrs: ${extra}`);
    paths.push(d);
  }
  if (paths.length === 0) throw new Error(`${id} (${slug}): no foreground paths`);
  return paths;
}

const icons = {};
const authors = new Set();
const failures = [];
// A little concurrency, politely bounded.
const POOL = 8;
let i = 0;
await Promise.all(
  Array.from({ length: POOL }, async () => {
    while (i < entries.length) {
      const [id, slug] = entries[i++];
      try {
        icons[id] = extractPaths(id, slug, await fetchIcon(slug));
        authors.add(slug.split('/')[0]);
      } catch (err) {
        failures.push(String(err.message ?? err));
      }
    }
  }),
);

if (failures.length) {
  console.error(`\n${failures.length} FAILURES:`);
  for (const f of failures) console.error('  -', f);
  process.exit(1);
}

const lines = entries.map(
  ([id]) => `  ${JSON.stringify(id)}: [${icons[id].map((d) => JSON.stringify(d)).join(', ')}],`,
);
const out = `// AUTO-GENERATED by scripts/fetch-game-icons.mjs — do not edit by hand.
// Icon art from https://game-icons.net (CC BY 3.0).
// Authors: ${[...authors].sort().join(', ')}.
// Source slugs live in src/content/icon-map.json.
export const ICON_PATHS: Record<string, string[]> = {
${lines.join('\n')}
};
`;
writeFileSync(OUT_FILE, out);
console.log(`wrote ${OUT_FILE}: ${entries.length} icons, ${[...authors].length} authors`);
